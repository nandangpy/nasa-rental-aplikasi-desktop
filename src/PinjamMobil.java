    public void idPelanggan(){
        
        try {
            Connection konek = new KonekDB().getConnection();
            Statement stat = konek.createStatement();
            String sql = "SELECT MAX(id_pelanggan) FROM tb_pelanggan";
            ResultSet hasil = stat.executeQuery(sql);
            
            if (hasil.next()) {
                String noPel = hasil.getString(1);
                String idPel = "" + (Integer.parseInt(noPel) + 1);

               noPelangganTxt.setText(idPel);
            } else {
               noPelangganTxt.setText("105");
            }
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        //    JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
