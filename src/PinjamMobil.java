    private void lanjutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lanjutBtnActionPerformed
        // TODO add your handling code here:
        if (namaPeminjamTxt.getText().isEmpty() ||
            alamatTxt.getText().isEmpty() || 
            teleponTxt.getText().isEmpty() ||
            noKtpTxt.getText().isEmpty()) { 
            pesanGagal.setText("Data Harus Lengkap..!!!");

        } else {
            String id = noPelangganTxt.getText();
            String nama = namaPeminjamTxt.getText();
            String alamat = alamatTxt.getText();
            String ktp = noKtpTxt.getText();
            String tlp = teleponTxt.getText();
            
            Home.jDesktopPane1.removeAll();
            Home.jDesktopPane1.repaint();
            TransaksiMobil fm = new TransaksiMobil(id, nama, alamat, ktp ,tlp);
            
            fm.setVisible(true);
            Home.jDesktopPane1.add(fm);
        }
        
    }//GEN-LAST:event_lanjutBtnActionPerformed
    
   
